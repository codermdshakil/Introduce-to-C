#include <stdio.h>

int main(){

    int a = 10;
    int b = 11;

// not equal 
    if (a != b)
    {
        printf("You are right\n");
    }else{
        printf("You are Wrong");
    }

// Equal 
if (a == b)
    {
        printf("You are right\n");
    }else{
        printf("You are Wrong");
    }

// less then 
if (a < b)
    {
        printf("You are right\n");
    }else{
        printf("You are Wrong");
    }

// less then Equal
if (a <= b)
    {
        printf("You are right\n");
    }else{
        printf("You are Wrong");
    }
    
    

// grater then 
if (a > b)
    {
        printf("You are right\n");
    }else{
        printf("You are Wrong");
    }

// grater then equal
if (a >= b)
    {
        printf("You are right\n");
    }else{
        printf("You are Wrong");
    }

    
    return 0;
}

/**
 * Relational Operators
 * - < → Less then
 * ≤ → Less then and equal
 * > → Grater then
 * ≥ → Grater then and Equal
 * == → Equal
 * ≠ → Not Equal
 * 
*/